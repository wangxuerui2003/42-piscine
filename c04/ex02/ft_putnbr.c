#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	c = 0;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		c += (nb + '0');
		write(1, &c, 1);
	}
}
